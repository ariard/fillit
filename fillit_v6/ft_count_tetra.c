#include "fillit.h"
#include <stdio.h>

size_t		ft_count_tetra(char *s)
{
	size_t			x;
	size_t			y;
	size_t			i;

	i = 1;
	x = 1;
	y = 1;
	while (*s)
	{
		if (y % 5 == 0 && *s == 10)
			i++;
		if ((x == 5 && *s == 10) || (y % 5 == 0 && *s == 10))
		{
				x = 1;
				y++;
				s++;
		}
		else if (*s++)
			x++;
	}
	return (i + 1);
}
